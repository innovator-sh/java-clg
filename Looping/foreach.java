/* 
 * Using For Each loop (Enhanced For loop) to display all the array elements
*/

public class foreach {
    public static void main(String args[]) {

        int a[] = {1, 2, 3, 4, 5};

        System.out.println("Array Elements:");
        for (int i : a) {
            System.out.println(i);
        }

    }
}